a = float(input("Enter the first number (A): "))
b = float(input("Enter the second number (B): "))

sum = a + b
difference = a - b
product = a * b
division = a / b
exponentiation = a ** b
floor_division = a // b
modulus = a % b

print("A + B =", sum)
print("A - B =", difference)
print("A * B =", product)
print("A / B =", division)
print("A ** B =", exponentiation)
print("A // B =", floor_division)
print("A % B =", modulus)