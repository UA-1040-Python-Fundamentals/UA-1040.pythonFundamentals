a = float(input("A="))
b = float(input("B="))

sum = a + b
difference = a - b
product = a * b
division = a / b
exponentiation = a ** b
floor = a // b
modulo = a % b

print("A + B =", sum)
print("A - B =", difference)
print("A * B =", product)
print("A / B =", division)
print("A ** B =", exponentiation)
print("A // B =", floor)
print("A % B =", modulo)