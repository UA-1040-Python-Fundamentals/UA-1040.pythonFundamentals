a = float(input("Input A: "))
b = float(input("Input B: "))

sum = a + b
diff = a - b
product = a * b
division = a / b
exponentiation = a ** b
integer_division = a // b
division_with_a_remainder = a % b

print("A + B = ", sum)
print("A - B = ", diff)
print("A * B = ", product)
print("A / B = ", division)
print("A**B = ", exponentiation)
print("A//B = ", integer_division)
print("A%B = ", division_with_a_remainder)